void	ft_two_pow(int *n)
{
	int	tmp;

	tmp = *n;
	*n = (1 << tmp);
}

int	ft_sqrt(int n)
{
	int	i;

	i = 1;
	while (i * i <= n)
	{
		if (i * i == n)
			return (i);
		i++;
	}
	return (0);
}
